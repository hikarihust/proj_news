        <p><em>Sports Mail</em>&nbsp;(Anh)&nbsp;cho biết, án phạt cấm tham dự Champions
            League một mùa với Man City, do vi phạm luật công bằng tài chính (FFP), chỉ được
            đưa ra sớm nhất vào mùa 2020-2021.</p>
        <p>Trong bức thư ngỏ gửi tới truyền thông Anh, Man City viết: "Chúng tôi hợp tác một
            cách thiện chí với Tiểu ban kiểm soát tài chính các CLB của UEFA (CFCB). CLB tin
            tưởng vào sự độc lập và cam kết của CFCB hôm 7/3, rằng sẽ không kết luận gì
            trong thời gian điều tra".</p>
